void solve()
{
      int n, k;
      cin >> n >> k;
      int ans = k / n;
      int rem = k % n;
      if (rem > 0)
      {
            cout << ans + 1 << '\n';
      }
      else
      {
            cout << ans << '\n';
      }
}

